#include <stdio.h>
int main() {
    int x = 5, y = 10;

    printf("%d == %d : %d\n", x, y, x == y);
    printf("%d != %d : %d\n", x, y, x != y);
    printf("%d > %d : %d\n", x, y, x > y);
    printf("%d < %d : %d\n", x, y, x < y);
    printf("%d >= %d : %d\n", x, y, x >= y);
    printf("%d <= %d : %d\n", x, y, x <= y);

    return 0;
}
